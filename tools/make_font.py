def main():
    px = [[(255, 255, 255, 0)] * W for _ in range(H)]
    for code in range(32, 128):
        ch = chr(code)
        glyph = G.get(ch.upper() if ch.isalpha() else ch)
        if not glyph:
            continue
        idx = code - 32
        cx = (idx % COLS) * CELL
        cy = (idx // COLS) * CELL
        for gy, rowbits in enumerate(glyph):
            for gx, bit in enumerate(rowbits):
                if bit == '1':
                    px[cy + gy][cx + gx] = (255, 255, 255, 255)

    raw = bytearray()
    for y in range(H):
        raw.append(0)
        for x in range(W):
            raw += bytes(px[y][x])

    def chunk(tag, data):
        c = tag + data
        return struct.pack(">I", len(data)) + c + struct.pack(">I", zlib.crc32(c) & 0xFFFFFFFF)

    sig  = b"\x89PNG\r\n\x1a\n"
    ihdr = struct.pack(">IIBBBBB", W, H, 8, 6, 0, 0, 0)
    idat = zlib.compress(bytes(raw), 9)
    out = os.path.normpath(os.path.join(
        os.path.dirname(__file__), "..", "client", "assets", "textures", "font.png"))
    with open(out, "wb") as f:
        f.write(sig + chunk(b"IHDR", ihdr) + chunk(b"IDAT", idat) + chunk(b"IEND", b""))
    print("wrote", out, f"({W}x{H})")

if __name__ == "__main__":
    main()
